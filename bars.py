"""Bars Module"""
def starbar(num):
    """打印 * 分割线
     :arg num: 线长
     """
    print('*' * num)

def hashbar(num):
    """打印 # 分割线
     :arg num: 线长
     """
    print('#' * num)

def simplebar(num):
    """打印 - 分割线
     :arg num: 线长
     """
    print('-' * num)

# 这就是创建了一个模块。模块就是包括 Python 定义和声明的文件，后加上 .py 后缀。
